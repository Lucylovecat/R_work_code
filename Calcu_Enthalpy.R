Calcu_Enthalpy<-function(t, rh, atm){  
  Ta = t + 273.15 
  C = c(-5.6745359e3,6.3925247,-9.6778430e-3,6.2215701e-7,2.0747825e-9,
        -9.4840240e-13,4.1635019,-5.8002206e3,1.3914993,-4.8640239e-2,
        4.1764768e-5,-1.4452093e-8,6.5459673)
  if (t < 0.0){
    pv = rh / 100.0 * (0.001*exp(C[1]/Ta+C[2]+C[3]*Ta+C[4]*(Ta^2)+C[5]*(Ta^3)+C[6]*(Ta^4)+C[7]*log(Ta)))
  }else{ 
    pv = rh / 100.0 *(0.001*exp(C[8]/Ta+C[9]+C[10]*Ta+C[11]*(Ta^2)+C[12]*(Ta^3)+C[13]*log(Ta))) 
  } 
  
  h = 1.006 * t + (0.621945 * pv / ((atm/10) - pv)) * (2501 + 1.86 * t) 
  
  return (h) 
}
